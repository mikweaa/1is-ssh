export function getElectiveOptions(
  branchId: string | null | undefined,
  degreeId: string | null | undefined,
  semesterId: string | null | undefined
): string[] {
  if (!branchId || !degreeId || !semesterId) return [];
  // Medicine integrated years 4–6 (semesters 7–9): blank-slate semesters – return all selectable subjects (mandatory + electives)
  if (branchId === "MD" && degreeId === "integrated" && ["7", "8", "9"].includes(semesterId)) {
    const mandatory = getSemesterMandatoryCourses(branchId, degreeId, semesterId);
    const mandatoryNames = mandatory.map((c) => c.name);
    const electiveNames = getMedElectiveOptions(semesterId);
    return [...mandatoryNames, ...electiveNames];
  }
  if (branchId === "MD") return getMedElectiveOptions(semesterId);
  if (branchId === "DM") return getDentalElectiveOptions(semesterId);

  const branch = BRANCH_ELECTIVES[branchId];
  const degree = branch?.[degreeId];
  
  // For PhD programs, always return PhD electives regardless of semester number
  const isPhdProgram = degreeId === "phd" || degreeId === "phd-ib" || degreeId === "phd-fin";
  if (isPhdProgram && degree) {
    // Get the PhD electives defined at semester "1" (same for all PhD semesters)
    const phdElectives = degree["1"] ?? [];
    return phdElectives;
  }
  
  const options = degree?.[semesterId] ?? [];
  const filtered = options.filter(
    (option) =>
      !/TURK111|TURK112|BOS111|BOS112|Spoken Turkish|Spoken Bosnian|Spoken Language/i.test(
        option
      )
  );

  // Foreign language electives are now handled separately in Dashboard
  // Don't add them here anymore
  return filtered;
}
